package fundamentos.operadores;

public class Unarios {
	private static final int ba = 0;

	public static void main(String[] args) {
		
		int a = 1;
		a++; // a = a + 1
		a--; // a = a - 1
		
		//++b; // b = b + 1
		//--b; // b = b - 1
		
		
		System.out.println(a + ba);
		System.out.println(ba);
	}
}
