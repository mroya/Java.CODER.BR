package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);
		
		var b = 4.5;
		System.out.println(b);
		
		var c = "texto";
		System.out.println(c);

		c = "outro texto";
		System.out.println(c);
		
		double d;
		d = 13.65;
		System.out.println(d);
	}
}
